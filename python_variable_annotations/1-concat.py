#!/usr/bin/env python3
"""Module concat
Provides a function to concatenate two strings.
"""

def concat(a: str, b: str) -> str:
    """Concatenate two strings and return the result.

    Args:
        a (str): The first string.
        b (str): The second string.

    Returns:
        str: The concatenation of a and b.
    """
    return a + b
